<div class="jumbotron">
    <h1 style="font-weight:600;">Consultas</h1>
    <hr class="my-4">
    <p>Para poder realizar una consulta primero debe seleccionar los filtros 
        deseados que se muestran en a continuación: </p>
    <img src="../img/userGuide1.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide1"><br>
    
    <p>El usuario puede seleccionar todos los filtros mediante el botón 
       <b>Seleccionar todos</b>. También se le ofrece la posibilidad de descartar todos los filtros que haya seleccionado pulsando el botón <b>Eliminar filtros</b> para poder 
       realizar una nueva consulta.</p>
    
    <p>Los filtros seleccionados aparecerán en la barra lateral izquierda para 
        permitir al usuario acceder a cada una de ellas para poder rellenarlas de 
        forma cómoda y rápida. </p>

    <p>Para poder realizar la consulta el usuario debe pulsar en <b>“Realizar consulta”</b> 
       que se encuentra en la barra de navegación lateral izquierda.</p>

    <p>A modo de ejemplo se describirá a continuación la realización de una consulta 
        paso a paso para mostrar aquellos pacientes <i>con un PSA preoperatorio con 
        un valor mayor a 20 ng/ml:</i></p>
    <ol>
        <li>Seleccionar el filtro <b>Clínico patológico</b>, el cual se mostrará en la barra 
            lateral izquierda una vez que se seleccione en los filtros.</li><br>
        <img src="../img/userGuide2.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide2"><br>

        <li>En el filtro <b>Clínico patológico</b> se muestra la variable <b>PSA preoperatorio</b> que 
        tiene como valores a seleccionar los que aparecen en la siguiente imagen. Seleccionar el valor 
        <b>más de 20 ng/ml:</b></li><br>
        <img src="../img/userGuide3.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide3"><br>

        <li>Seleccionar la opción <b>Realizar consulta</b> que aparece en la barra lateral izquierda y aparecerá 
            una tabla con los pacientes que coincidan con la consulta realizada:</li><br>
        <img src="../img/userGuide4.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide4">    
    </ol>
    <br>

    <p>Los valores correspondientes de cada columna se describen en el archivo descargable que se encuentra en la 
        página de <b>Pacientes</b> pulsando el botón <b>Descargar descripción de variables</b>.</p>
    
    <p>Una vez que la consulta se ha realizado, además de ver la tabla, el usuario puede:</p>
    <ul>
        <li><b>Ver estadísticas</b>: Se muestran gráficas sobre las variables <i>ETNIA, EDAD, TABACO, OBESIDAD, RBQ, ECOTR, TACTOR y HEREDA</i> sobre la consulta realizada.</li><br>
        <img src="../img/userGuide5.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide5"><br>

        <li><b>Ver detalles</b>: En esta página se muestran todas las variables que hay 
            en la base de datos de la aplicación web las cuales el usuario 
            puede excluir aquellas que considere y a continuación pulsar el botón 
            <b>Pulse aquí para ver los detalles</b> que aparece al final de la página.
            <img src="../img/userGuide6.png" class="d-block w-100" style="border: 20px solid #FFF;margin-top:20px;" alt="userGuide6"><br>

            <p>Al pulsar el botón se mostrará en una nueva ventana una serie de estadísticas 
            y gráficas con información muy detalla de la consulta con las variables que el usuario no ha descartado.</p><br>
            <img src="../img/userGuide7.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide7"><br>

            <p>En esta página se puede observar una opción que es <b>Generar detalles extendidos</b> que muestra información aún más detallada e interactiva</p><br>
            <img src="../img/userGuide8.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide8"><br>
            <img src="../img/userGuide9.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide9"><br>

        </li>
        
        <li><b>Exportar consulta</b>: Para exportar los datos de la consulta, el usuario debe pulsar el botón <b>Exportar datos</b>. Los datos se exportarán en formato <b>.xlsx</b>.</li><br>
        <img src="../img/userGuide10.png" class="d-block w-100" style="border: 20px solid #FFF;" alt="userGuide10"><br>

        

<div class="jumbotron">
    <h1 style="font-weight:600;">Pacientes</h1>
    <hr class="my-4">
    <p>En la barra lateral izquierda de dicha página se muestra las 
        opciones que se pueden realizar:</p>
    <ul>
        <li><b>Visualizar pacientes:</b> Muestra una tabla con los datos 
            de cada paciente y permite realizar la búsqueda de un paciente 
            por su ID. Los valores correspondientes de cada columna se describen 
            en el archivo descargable pulsando el botón 
            <b>Descargar descripción de variables</b>.</li>
        <li><b>Añadir paciente:</b> Muestra un formulario donde aparecen las 
            variables principales que hay en la base de datos. Una vez 
            rellenados los campos pulsar el botón <b>Añadir</b>.</li>
        <li><p><b>Importar pacientes:</b> Para importar los datos de los pacientes 
        primero hay que pulsar el botón <b>Seleccionar archivo</b> para seleccionar el 
        archivo deseado y después pulsar el botón de <b>Importar</b>.</p>
        <p>Asegúrese de que el archivo cumpla los siguientes requisitos:</p>
        <ul>
            <li>Los ficheros admitidos son de extensión <b>.xls y xlsx</b>.</li>
            <li>La información dentro del Excel debe empezar en la primera fila 
                que serán las columnas. Cualquier campo de un paciente que no esté 
                incluido en la Base de Datos será descartado.</li> 
        </ul><br>
        <p>En la página de <b>Pacientes</b> se ofrece un archivo Excel que puede usar a modo 
            de plantilla para rellenarlo con los datos de pacientes que quiera 
            introducir. Este Excel contiene las variables principales que tiene la 
            base de datos de la aplicación web. Para descargar la plantilla pulse 
            el botón <b>Descargar plantilla Excel</b> en la sección <b>Importar pacientes</b>.</p>
        
        <p>La descripción de cada variable se describe en el archivo descargable que 
            se encuentra en la página de <b>Pacientes</b> pulsando el botón 
            <b>Descargar descripción de variables</b>.</p>
        </li>
        <li><b>Exportar pacientes:</b> Permite exportar todos los datos de pacientes 
            que hay almacenados en la base de datos de la aplicación web. Para exportarlos 
            solo tiene que pulsar el botón <b>Exportar datos</b>.</li>
    </ul>
</div>